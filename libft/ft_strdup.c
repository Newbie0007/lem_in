#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char *c;

	c = (char*)malloc(sizeof(*s1) * (ft_strlen(s1) + 1));
	if (c)
		ft_strcpy(c, s1);
	return (c);
}
